package dk.itu.raptor.join;

public class PixelRange {
    int rid, tid, gid, y, x1, x2;

    public PixelRange(int rid, int tid, int gid, int y, int x1, int x2) {
        this.rid = rid;
        this.tid = tid;
        this.gid = gid;
        this.y = y;
        this.x1 = x1;
        this.x2 = x2;
    }
}
